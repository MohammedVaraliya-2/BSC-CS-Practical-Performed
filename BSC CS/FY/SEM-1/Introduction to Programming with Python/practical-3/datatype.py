# Text Types:
# 1.str:
x = "Hello World"
print(x)
print(type(x))

# Numeric Type:
# 1.int
# 2.float
# 3.complex

# int
x = 20
print(x)
print(type(x))

# float
x = 20.5
print(x)
print(type(x))

# complex
x = 1j
print(x)
print(type(x))

# Sequence Types:
# 1.list
# 2.tuple
# 3.range

# list
x = ["apple", "banana", "cherry"]
print(x)
print(type(x))

# tuple
x = ("apple", "banana", "cherry")
print(x)
print(type(x))

# range
x = range(6)
print(x)
print(type(x))

# Mapping Type:
# 1.dict

# dict
x = {"name" : "John", "age" : 36}
print(x)
print(type(x))

# Set Types:
# 1.set
# 2.frozenset

# set
x = {"apple", "banana", "cherry"}
print(x)
print(type(x))

# frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x)
print(type(x))

# Boolean Type:
# 1.bool

# bool
x = True
print(x)
print(type(x))

# BINARY TYPES:
# 1.byte
# 2.bytearray
# 3.memory view

# byte
x = b"Hello"
print(x)
print(type(x))

# bytearray
x = bytearray(5)
print(x)
print(type(x))

# memoryview
x = memoryview(bytes(5))
print(x)
print(type(x))
